/**
 * Get holidays for a specific year
 */
export const getHolidaysForYear = (year) => {
  return INDIAN_HOLIDAYS[year.toString()] || [];
};

/**
 * Get holidays for a specific date
 */
export const getHolidaysForDate = (date) => {
  const year = date.getFullYear().toString();
  const dateStr = date.toISOString().split('T')[0];
  const holidays = INDIAN_HOLIDAYS[year] || [];
  return holidays.filter(h => h.date === dateStr);
};

/**
 * Convert holidays to event-like objects for display
 */
export const holidaysToEvents = (holidays, showHolidays = true) => {
  if (!showHolidays) return [];
  
  return holidays.map(holiday => {
    const date = new Date(holiday.date);
    date.setHours(0, 0, 0, 0);
    const endDate = new Date(date);
    endDate.setHours(23, 59, 59, 999);
    
    return {
      id: `holiday-${holiday.date}`,
      title: holiday.name,
      startTime: date,
      endTime: endDate,
      color: '#0b8043', // Green color for holidays
      location: '',
      description: '',
      isHoliday: true,
      holidayType: holiday.type,
    };
  });
};

